package HomeWork.Lesson2;

public class SistemaSchisleniy {
    public static void main(String[] args) {

        byte b1 = 12;
        byte b2 = 0b1100;
        byte b3 = 014;
        byte b4 = 0xC;
        System.out.println(b1 + " " + b2 + " " + b3 + " " + b4);

        float f1 = 4.3435F;
        float f2 = 5.3234F;

        double d1 = 125.12;
        double d2 = 111111.24;

        boolean bo1 = true;
        boolean bo2 = false;

        System.out.println(f1 + " " + f2 + " " + d1 + " " + d2 + " " + bo1 + " " + bo2);

        char c1 = 'a';
        char c2 = 16;
        char c3 = '\u0016';
        System.out.println(c1 + " " + c2 + " " + c3);

    }
}
